export interface PlatformDef {
  key: ChannelLinkPlatform;
  label: string;
  color: string; // фирменный hex (иконка)
  iconPath: string; // d пути simple-icons (viewBox 0 0 24 24)
  example: string; // подсказка в поле ввода
  /** Распарсенный URL → каноничная безопасная ссылка на профиль/канал, или null если это не она. */
  match: (u: URL) => string | null;
}

// hostname без ведущего www. (m./mobile. сверяем явно там, где нужно).
const bareHost = (u: URL): string => u.hostname.toLowerCase().replace(/^www\./, "");
const path = (u: URL): string => u.pathname.replace(/\/+$/, ""); // без хвостового слэша

/** Частый случай: один сегмент-ник на каноничном домене. */
function singleSegment(hosts: string[], canonical: string, re: RegExp): PlatformDef["match"] {
  return (u) => {
    if (!hosts.includes(bareHost(u))) return null;
    const m = re.exec(path(u));
    return m ? `https://${canonical}/${m[1]}` : null;
  };
}

